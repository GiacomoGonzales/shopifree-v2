export default function TrustBar() {
  const { store, theme, language } = useTheme()

  if (!store.trustBadges?.enabled || store.plan === 'free') return null

  const activeBadges = store.trustBadges.badges.filter(b => b.enabled)
  if (activeBadges.length === 0) return null

  return (
    <div
      className="py-4 px-4 overflow-x-auto scrollbar-hide"
      style={{ backgroundColor: theme.colors.surface }}
    >
      <div className="flex items-center justify-center gap-6 md:gap-10 min-w-max mx-auto max-w-6xl">
        {activeBadges.map((badge) => (
          <div key={badge.id} className="flex items-center gap-2 flex-shrink-0">
            <span style={{ color: theme.colors.primary }}>
              {badgeIcons[badge.id]}
            </span>
            <span
              className="text-xs md:text-sm font-medium whitespace-nowrap"
              style={{ color: theme.colors.textMuted }}
            >
              {badge.text || getTrustBadgeText(badge.id, language)}
            </span>
          </div>
        ))}
      </div>
    </div>
  )
}
